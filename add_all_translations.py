                    # –ï—Å–ª–∏ –≤ —Ç–µ–∫—Å—Ç–µ –µ—Å—Ç—å –∫–∏—Ä–∏–ª–ª–∏—Ü–∞ - —ç—Ç–æ —Ä—É—Å—Å–∫–∏–π —Ç–µ–∫—Å—Ç
                    if any('\u0400' <= c <= '\u04FF' for c in text):
                        # –ö–æ–ø–∏—Ä—É–µ–º msgid –≤ msgstr
                        result.append(f'msgstr "{text}"\n')
                        i += 2  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —Å–ª–µ–¥—É—é—â—É—é —Å—Ç—Ä–æ–∫—É
                        continue

